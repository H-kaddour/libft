#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s) - 1;
	while (s[i] != c && s[i])
	{
		if (s[i] == '\0')
			return (NULL);
		i--;
	}
	return (&s[i]);
}
